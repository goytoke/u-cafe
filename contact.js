    let currentLang = 'en';

    function updateLanguage(lang) {
      currentLang = lang;
      const t = translations[lang];
      if (!t) return;
      document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) el.innerHTML = t[key];
      });
      // update button flag and text
      const langBtn = document.getElementById('langBtn');
      const currentFlag = langBtn.querySelector('img');
      const currentNameSpan = document.getElementById('currentLangName');
      if (lang === 'en') {
        currentFlag.src = "https://flagcdn.com/w20/gb.png";
        currentNameSpan.innerText = "English";
      } else if (lang === 'am') {
        currentFlag.src = "ethiopia-flag.png";
        currentNameSpan.innerText = "አማርኛ";
      } else if (lang === 'or') {
        currentFlag.src = "oromia-flag.png";
        currentNameSpan.innerText = "Oromoo";
      } else if (lang === 'ti') {
        currentFlag.src = "tigray-flag.png";
        currentNameSpan.innerText = "ትግርኛ";
      }
    }

