    document.getElementById('reviewForm').addEventListener('submit',(e)=>{
      e.preventDefault();
      const rating = parseInt(document.getElementById('selectedRating').value);
      const content = document.getElementById('reviewContent').value.trim();
      const name = document.getElementById('reviewerName').value.trim()||"Anonymous";
      const title = document.getElementById('reviewTitle').value.trim()||"Review";
      if(!rating||rating<1||rating>5) { document.getElementById('reviewModalMessage').innerHTML='<span style="color:#c0392b;">⚠️ Please select rating.</span>'; return; }
      if(!content) { document.getElementById('reviewModalMessage').innerHTML='<span style="color:#c0392b;">⚠️ Please write review.</span>'; return; }
      addReview({ id:Date.now(), name, title, rating, comment:content, date:new Date().toLocaleDateString() });
      document.getElementById('reviewModalMessage').innerHTML='<span style="color:#2e7d32;">✅ Thank you!</span>';
      setTimeout(()=>{ modal.style.display='none'; },1500);
    });
    function initStars() {
      const stars = document.querySelectorAll('#starRatingWidget i');
      const ratingInput = document.getElementById('selectedRating');
      stars.forEach(star=>{
        star.addEventListener('click',function(){
          const val = parseInt(this.getAttribute('data-value'));
          ratingInput.value = val;
          stars.forEach(s=>{ const sv=parseInt(s.getAttribute('data-value')); if(sv<=val) s.className="fas fa-star"; else s.className="far fa-star"; });
        });
      });
    }
