    // --- REVIEW SYSTEM ---
    const STORAGE_KEY = "ucafe_user_reviews";
    function loadReviews() { const s = localStorage.getItem(STORAGE_KEY); return s ? JSON.parse(s) : []; }
    function saveReviews(r) { localStorage.setItem(STORAGE_KEY, JSON.stringify(r)); }
    function avgRating(reviews) { if(!reviews.length) return 0; return reviews.reduce((a,b)=>a+b.rating,0)/reviews.length; }
    function updateUserRatingUI() {
      const reviews = loadReviews();
      const avg = avgRating(reviews);
      const cnt = reviews.length;
      const starsDiv = document.getElementById('userStarsDisplay');
      if(starsDiv) {
        const stars = starsDiv.querySelectorAll('i');
        const full = Math.round(avg);
        for(let i=0;i<stars.length;i++) stars[i].className = i < full ? "fas fa-star" : "far fa-star";
      }
      const cntSpan = document.getElementById('userReviewCount');
      if(cntSpan) cntSpan.innerHTML = cnt===0 ? "0 reviews · Be the first!" : `${cnt} review${cnt!==1?'s':''} · ⭐ ${avg.toFixed(1)}/5`;
    }
    function addReview(r) { const revs = loadReviews(); revs.push(r); saveReviews(revs); updateUserRatingUI(); }

